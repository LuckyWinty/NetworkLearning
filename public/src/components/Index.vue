<script>
  import Subject from 'components/module/Subject.vue'

  export default {
    data () {
      return {
        currentDate: new Date(),
        hotLists: []
      }
    },
    components: {
      'Subject': Subject
    },
    created () {
      this.showSubjects()
    },
    methods: {
      getUrl () {
        return this.$store.state.basicUrl
      },
      popTip (title, tips) {
        this.$alert(tips, title, {
        })
      },
      showSubjects () {
        var self = this
        var userId = window.sessionStorage.getItem('userId') || ''
        this.$http.post(self.getUrl() + '/index', {userId: userId}).then((response) => {
          if (response.status === 200) {
            if (response.data.status === 1) {
              console.log('----------所有的课程', response.data.Subjects)
              self.hotLists = self.hotLists.concat(response.data.Subjects)
            } else {
              self.popTip(response.data.mes)
            }
          }
        }, (response) => {
          // error callback
        })
      }
    }
  }
</script>
